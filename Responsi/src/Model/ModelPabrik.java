package Model;
public class ModelPabrik {
    private int id;
    private String nama;
    private float unit;
    private float jamkerja;
    private float tenagakerja;
    private float bahanbaku;
    private float biaya;
    private float efisiensi;
    private float totalproduksi;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public float getUnit() {
        return unit;
    }

    public void setUnit(float unit) {
        this.unit = unit;
    }

    public float getJamkerja() {
        return jamkerja;
    }

    public void setJamkerja(float jamkerja) {
        this.jamkerja = jamkerja;
    }

    public float getTenagakerja() {
        return tenagakerja;
    }

    public void setTenagakerja(float tenagakerja) {
        this.tenagakerja = tenagakerja;
    }

    public float getBahanbaku() {
        return bahanbaku;
    }

    public void setBahanbaku(float bahanbaku) {
        this.bahanbaku = bahanbaku;
    }

    public float getBiaya() {
        return biaya;
    }

    public void setBiaya(float biaya) {
        this.biaya = biaya;
    }

    public float getEfisiensi() {
        return efisiensi;
    }

    public void setEfisiensi(float efisiensi) {
        this.efisiensi = efisiensi;
    }

    public float getTotalproduksi() {
        return totalproduksi;
    }

    public void setTotalproduksi(float totalproduksi) {
        this.totalproduksi = totalproduksi;
    }

    
    

}
